
class Math :
     
    def __init__(self,a,b):
            self.a =a 
            self.b = b
             
    def add(self):
        return self.a+self.b
    
    def subtract(self):
        return self.a-self.b
    
    def multiple(self):
        return self.a*self.b
    
    def division(self):
        return self.a/self.b
    
    def remainder(self):
        return self.a%self.b
    
